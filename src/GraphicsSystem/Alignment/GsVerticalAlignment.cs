
namespace GraphicsSystem
{
  /// <summary>
  /// Specifies alignment options in the vertical direction.
  /// </summary>
  public enum GsVerticalAlignment
  {
    /// <summary>
    /// Align to the top.
    /// </summary>
    Top = 0,

    /// <summary>
    /// Align to the center.
    /// </summary>
    Center = 1,

    /// <summary>
    /// Align to the bottom.
    /// </summary>
    Bottom = 2,

    /// <summary>
    /// Align to fill the entire layout width.
    /// </summary>
    Stretch = 3,

    /// <summary>
    /// Allow the client to pick the best alignment
    /// </summary>
    Auto = 4
  }
}
