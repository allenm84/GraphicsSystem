
namespace GraphicsSystem
{
  /// <summary>
  /// Specifies alignment options in the horizontal direction.
  /// </summary>
  public enum GsHorizontalAlignment
  {
    /// <summary>
    /// Align to the left.
    /// </summary>
    Left = 0,

    /// <summary>
    /// Align to the center.
    /// </summary>
    Center = 1,

    /// <summary>
    /// Align to the right.
    /// </summary>
    Right = 2,

    /// <summary>
    /// Align to fill the entire layout width.
    /// </summary>
    Stretch = 3,

    /// <summary>
    /// Allow the client to pick the best alignment
    /// </summary>
    Auto = 4
  }
}
